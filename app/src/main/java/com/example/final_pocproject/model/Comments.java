package com.example.final_pocproject.model;

public class Comments {

    private int id;

    private String name;

    private String email;

    private String body;


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getBody() {
        return body;
    }
}
